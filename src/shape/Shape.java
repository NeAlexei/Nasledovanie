package shape;

public class Shape {
    private String name;
    private String color;

    public Shape(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public void setName(){
        this.name = name;
    }

    public String getName(){
        return name;
    }

    public void setColor(){
        this.color = color;
    }

    public String getColor(){
        return color;
    }



}
